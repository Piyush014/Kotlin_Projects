package com.piyush.a06_recyclerview_demo;

public class LanguageData {
    private String title;
    private int logo;

    public LanguageData(String title, int logo) {
        this.title = title;
        this.logo = logo;
    }

    public String getTitle() {
        return title;
    }

    public int getLogo() {
        return logo;
    }
}
